def predict_senti(input_tw):
    tc = TextCounts()
    ct = CleanText()

    model_senti = load('model.joblib') 
    tweets = pd.Series(input_tw)
    df_counts_neg = tc.transform(tweets)
    df_clean_neg = ct.transform(tweets)
    df_test = df_counts_neg
    df_test['clean_text'] = df_clean_neg
    return model_senti.predict(df_test).tolist()